#include "headl.h" 
class Solution {
public:
    vector<int> findLonely(vector<int>& nums) {
        if (nums.size() <= 1)
        {
            return nums;
        }

        sort(nums.begin(), nums.end());
        vector<int> ans;
        if (((nums[0] + 1) != nums[1]) and (nums[0] != nums[1])) ans.push_back(nums[0]);
        for (int i = 1; i < nums.size() - 1; i++)
        {
            if ((nums[i] != nums[i + 1]) and (nums[i] != nums[i - 1]) and ((nums[i] - 1) != nums[i - 1]) and ((nums[i] + 1) != nums[i + 1]))
            {
                ans.push_back(nums[i]);
            }
        }
        if (((nums[nums.size() - 1] - 1) != nums[nums.size() - 2]) and (nums[nums.size() - 1] != nums[nums.size() - 2]) )
        {
            ans.push_back(nums[nums.size() - 1]);
        }
        return ans;
    }
};
